package com.example.think.uihealth.widget;

import android.content.Context;
import android.util.AttributeSet;
import android.widget.LinearLayout;

/**
 * Created by kermit on 15-11-13.
 */
public class AutoAddViewLayout extends LinearLayout {

    public AutoAddViewLayout(Context context) {
        super(context);
    }

    public AutoAddViewLayout(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public AutoAddViewLayout(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }

    @Override
    protected void onLayout(boolean changed, int l, int t, int r, int b) {
        super.onLayout(changed, l, t, r, b);
    }
}
